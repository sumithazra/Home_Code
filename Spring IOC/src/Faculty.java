
public class Faculty {

	
	private String fname;
	private String FacultyID;
	
	
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFacultyID() {
		return FacultyID;
	}
	public void setFacultyID(String facultyID) {
		FacultyID = facultyID;
	}
	
}
